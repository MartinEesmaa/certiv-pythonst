# Access elements in Dictionary

"""
To access dictionary elements, you can use the familiar square brackets along with
the key to obtain the corresponding value.
"""

dict = {'Name': 'Zara', 'Age': 7, 'Class': 'First'}
print("dict['Name']: ", dict['Name'])
print("dict['Age']: ", dict['Age'])

# When above code is executed, produces result as follows:

"""
dict['Name']:  Zara
dict['Age']:  7
"""