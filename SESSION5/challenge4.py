# Question 4: Slicing Lists

"""
Q11: Create a list numbers with elements for every tens only
from 10 to 80. Use slicing to print the following.

elements = [10, 20, 30, 40, 50, 60, 70, 80]
A) The first four elements
B) The last three elements
C) Every second element from the list
"""

elements = [10, 20, 30, 40, 50, 60, 70, 80]
print(elements[:4])
print(elements[-3:])
print(elements[::2])