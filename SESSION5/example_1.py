# How to create a list

"""
A list is created by placing all the elements inside a square bracket []
Elements are separated by commas.
Can have any number of elements
They may be different types such as an integer, float, string and more.
"""

# Create an empty list:
empty_list = []

# Create a list of integers
integer_list = [1, 2, 3]

# Create a list with mixed data types
mixed_list = [1, "Hello", 3.4]