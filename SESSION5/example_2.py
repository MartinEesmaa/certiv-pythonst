# How to use a list

"""
Access the elements of a list
- Use the index operator [] to access an item in a list.
- The index starts from 0 at beginning.
- The list having 5 elements will have index from 0 to 4.
"""

state_list = ['Harjumaa', 'Hiiumaa', 'Ida-Virumaa', 'Jõgevamaa', 'Järvamaa']

# Output: Harjumaa
print(state_list[0])

# Output: Jõgevamaa
print(state_list[3])